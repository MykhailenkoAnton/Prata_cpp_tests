int amount_ocean(std::vector<std::vector<int>> arr)
{
    int count_ocean = 0;
    for (int i = 0; i < arr.size(); i++)
    {
        for (int j = 0; j < arr[i].size(); j++)
        {
            if (arr[i][j] == 1)
            {
                count_ocean++;
                
            }
            
        }
        
    }
    return count_ocean;
}
